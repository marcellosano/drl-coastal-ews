# SECTION 5: ACTION SPACE AND MASKING
# =============================================================================
# Action Space:
# 0: Monitor         - Passive observation, no protective action
# 1: Advisory        - Issue shelter-in-place advisory (effective for wind)
# 2: Close Beach     - Close beach accesses to prevent wave casualties
# 3: Sandbag Coast   - Deploy sandbags in coastal zone (effective for surge)
# 4: Sandbag River   - Deploy sandbags along rivers (effective for rain/flooding)
# 5: Evac High Risk  - Evacuate HIGH and VERY HIGH risk houses
# 6: Evac Coastal    - Evacuate coastal zone (x >= 14)
# 7: Evac River      - Evacuate riverside areas
# 8: Activate Prot   - Activate global protection systems
# 9: Full Evac       - Full community evacuation
#
# Storm-Appropriate Actions:
# - SURGE: Sandbag Coast (3), Evac Coastal (6), Close Beach (2)
# - RAIN:  Sandbag River (4), Evac River (7), Activate Prot (8)
# - WIND:  Advisory (1), Activate Prot (8)
# - MIXED: Full Evac (9), combined coastal+river protection

class ActionMasker:
    """Enhanced masking with Monitor masking at high intensity"""

    def __init__(self, config: Config):
        self.config = config
        self.action_names = ['Monitor', 'Advisory', 'Close Beach', 'Sandbag Coast',
                            'Sandbag River', 'Evac High', 'Evac Coast', 'Evac River',
                            'Activate Prot', 'Full Evac']

    def get_action_mask(self, env) -> np.ndarray:
        mask = np.ones(self.config.num_actions, dtype=np.float32)

        houses = env.houses
        threshold = self.config.mask_completion_threshold
        max_forecast = self._get_max_forecast_intensity(env)
        current_intensity = env.storm.intensity
        storm_type = env.storm.storm_type

        # === MONITOR MASKING AT HIGH INTENSITY ===
        # When intensity > threshold, Monitor action is MASKED
        # This forces the agent to choose an active protective action
        if self.config.use_monitor_masking:
            if current_intensity > self.config.monitor_mask_intensity_threshold:
                mask[0] = 0  # Mask Monitor action



        # When storm is SURGE, RAIN, or MIXED AND intensity > threshold, Advisory is MASKED
        # This forces the agent to use storm-appropriate actions
        # SURGE/RAIN: Need sandbags and evacuation
        # MIXED: Need Full Evac - people will drown if they shelter in place!
        # WIND only: Advisory remains available (shelter-in-place is correct)
        advisory_masked = False
        if self.config.use_advisory_masking:
            if storm_type in [StormType.SURGE, StormType.RAIN, StormType.MIXED]:
                if current_intensity > self.config.advisory_mask_intensity_threshold:
                    mask[1] = 0  # Mask Advisory action
                    advisory_masked = True

        # LAYER 1: SEVERITY GATING
        if max_forecast < self.config.medium_action_severity_threshold:
            mask[5] = 0  # Evac High
            mask[6] = 0  # Evac Coast
            mask[7] = 0  # Evac River
            mask[8] = 0  # Activate Protection
            mask[9] = 0  # Full Evac

        elif max_forecast < self.config.heavy_action_severity_threshold:
            mask[9] = 0  # Full Evac

        elif max_forecast < self.config.full_evac_severity_threshold:
            if env.current_step >= self.config.full_evac_deadline_step:
                mask[9] = 0

        # LAYER 2: REDUNDANCY MASKING
        if all(h.advisory_received for h in houses):
            mask[1] = 0

        if not any(env.infrastructure.beach_open):
            mask[2] = 0

        coastal_houses = [h for h in houses if h.position[0] >= 14]
        if coastal_houses:
            coastal_sandbag_rate = sum(1 for h in coastal_houses if h.coastal_sandbag) / len(coastal_houses)
            if coastal_sandbag_rate >= threshold:
                mask[3] = 0

        riverside_houses = [h for h in houses if env._is_riverside(h)]
        if riverside_houses:
            riverside_sandbag_rate = sum(1 for h in riverside_houses if h.riverside_sandbag) / len(riverside_houses)
            if riverside_sandbag_rate >= threshold:
                mask[4] = 0

        high_risk_houses = [h for h in houses if h.risk_level in ['HIGH', 'VERY HIGH']]
        if high_risk_houses:
            high_evac_rate = sum(1 for h in high_risk_houses if h.evacuated) / len(high_risk_houses)
            if high_evac_rate >= threshold:
                mask[5] = 0

        if coastal_houses:
            coastal_evac_rate = sum(1 for h in coastal_houses if h.evacuated) / len(coastal_houses)
            if coastal_evac_rate >= threshold:
                mask[6] = 0

        if riverside_houses:
            river_evac_rate = sum(1 for h in riverside_houses if h.evacuated) / len(riverside_houses)
            if river_evac_rate >= threshold:
                mask[7] = 0

        if all(h.protected for h in houses):
            mask[8] = 0

        total_evac_rate = sum(1 for h in houses if h.evacuated) / len(houses)
        if total_evac_rate >= threshold:
            mask[9] = 0

        # Safety: ensure at least one action is available
        if mask.sum() == 0:

            # Instead of always falling back to Close Beach (which steered 's policy),
            # use the CORRECT action for each storm type as fallback.
            # This ensures the fallback (when reached) is actually helpful.
            monitor_masked = self.config.use_monitor_masking and current_intensity > self.config.monitor_mask_intensity_threshold

            if monitor_masked and advisory_masked:
                # Both Monitor and Advisory masked - use STORM-SPECIFIC fallback
                # This is the key change from 's generic Close Beach fallback
                if storm_type == StormType.SURGE:
                    mask[3] = 1  # Sandbag Coast - correct for coastal flooding
                elif storm_type == StormType.RAIN:
                    mask[4] = 1  # Sandbag River - correct for river flooding
                elif storm_type == StormType.MIXED:

                    mask[9] = 1  # Full Evac - get everyone out before they drown
                else:  # WIND - shouldn't reach here since Advisory not masked for WIND
                    mask[1] = 1  # Advisory - correct for shelter-in-place
            elif monitor_masked:
                # Only Monitor masked - enable Advisory as fallback
                mask[1] = 1
            else:
                mask[0] = 1  # Fallback to Monitor

        return mask

    def _get_max_forecast_intensity(self, env) -> float:
        max_intensity = env.storm.intensity
        for i in range(min(self.config.forecast_horizon, len(env.storm_trajectory) - env.current_step)):
            future_idx = min(env.current_step + i, len(env.storm_trajectory) - 1)
            max_intensity = max(max_intensity, env.storm_trajectory[future_idx].intensity)
        return max_intensity


