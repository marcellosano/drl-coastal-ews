class MultiHeadAttention(nn.Module):
    def __init__(self, dim: int, num_heads: int = 4):
        super().__init__()
        self.num_heads, self.dim, self.head_dim = num_heads, dim, dim // num_heads
        self.qkv = nn.Linear(dim, dim * 3)
        self.out = nn.Linear(dim, dim)
        self.scale = self.head_dim ** -0.5

    def forward(self, x):
        B, N, C = x.shape
        qkv = self.qkv(x).reshape(B, N, 3, self.num_heads, self.head_dim).permute(2, 0, 3, 1, 4)
        q, k, v = qkv[0], qkv[1], qkv[2]
        attn = (q @ k.transpose(-2, -1)) * self.scale
        attn = attn.softmax(dim=-1)
        return self.out((attn @ v).transpose(1, 2).reshape(B, N, C))

class PPOActorCritic(nn.Module):
    """Storm-conditioned policy heads

    Instead of a single actor head for all storms, we have 4 separate heads.
    Each storm type gets its own action distribution, allowing MIXED to learn
    combination responses without competing with single-action policies.
    """
    def __init__(self, config: Config, obs_dim: int):
        super().__init__()
        self.config = config

        # Shared encoder (same as before)
        self.encoder = nn.Sequential(
            nn.Linear(obs_dim, config.hidden_dim), nn.LayerNorm(config.hidden_dim), nn.ReLU(), nn.Dropout(config.dropout),
            nn.Linear(config.hidden_dim, config.hidden_dim), nn.LayerNorm(config.hidden_dim), nn.ReLU(), nn.Dropout(config.dropout)
        )
        self.attention = MultiHeadAttention(config.hidden_dim, config.num_attention_heads)


        # Index: 0=SURGE, 1=RAIN, 2=WIND, 3=MIXED
        self.actor_surge = nn.Sequential(nn.Linear(config.hidden_dim, config.attention_dim), nn.ReLU(), nn.Dropout(config.dropout), nn.Linear(config.attention_dim, config.num_actions))
        self.actor_rain = nn.Sequential(nn.Linear(config.hidden_dim, config.attention_dim), nn.ReLU(), nn.Dropout(config.dropout), nn.Linear(config.attention_dim, config.num_actions))
        self.actor_wind = nn.Sequential(nn.Linear(config.hidden_dim, config.attention_dim), nn.ReLU(), nn.Dropout(config.dropout), nn.Linear(config.attention_dim, config.num_actions))
        self.actor_mixed = nn.Sequential(nn.Linear(config.hidden_dim, config.attention_dim), nn.ReLU(), nn.Dropout(config.dropout), nn.Linear(config.attention_dim, config.num_actions))

        # List for easy indexing
        self.actor_heads = [self.actor_surge, self.actor_rain, self.actor_wind, self.actor_mixed]

        # Shared critic (value estimation is storm-agnostic)
        self.critic = nn.Sequential(nn.Linear(config.hidden_dim, config.attention_dim), nn.ReLU(), nn.Dropout(config.dropout), nn.Linear(config.attention_dim, 1))

    def _extract_storm_type_idx(self, x: torch.Tensor) -> torch.Tensor:
        """Extract storm type index from observation one-hot encoding.
        Storm type is encoded at indices 10-13 in observation.
        Returns tensor of storm type indices (0=SURGE, 1=RAIN, 2=WIND, 3=MIXED)
        """
        # x shape: (batch, obs_dim) or (obs_dim,)
        if x.dim() == 1:
            storm_one_hot = x[10:14]
            return storm_one_hot.argmax().item()
        else:
            storm_one_hot = x[:, 10:14]  # (batch, 4)
            return storm_one_hot.argmax(dim=1)  # (batch,)

    def forward(self, x, action_mask: Optional[torch.Tensor] = None, storm_type_idx: Optional[torch.Tensor] = None):
        """Forward pass with storm-conditioned actor selection.

        Args:
            x: Observation tensor
            action_mask: Optional action mask
            storm_type_idx: Optional pre-computed storm type indices.
                           If None, extracted from observation.
        """
        features = self.attention(self.encoder(x).unsqueeze(1)).squeeze(1)

        # Get storm type indices if not provided
        if storm_type_idx is None:
            storm_type_idx = self._extract_storm_type_idx(x)

        # Handle batch vs single observation
        if isinstance(storm_type_idx, int) or (isinstance(storm_type_idx, torch.Tensor) and storm_type_idx.dim() == 0):
            # Single observation - use appropriate head
            head_idx = storm_type_idx if isinstance(storm_type_idx, int) else storm_type_idx.item()
            logits = self.actor_heads[head_idx](features)
        else:
            # Batch - need to select per-sample
            batch_size = features.shape[0]
            logits = torch.zeros(batch_size, self.config.num_actions, device=features.device)

            for i in range(4):  # For each storm type
                mask = (storm_type_idx == i)
                if mask.any():
                    logits[mask] = self.actor_heads[i](features[mask])

        if action_mask is not None:
            logits = logits + (1 - action_mask) * (-1e6)

        return logits, self.critic(features)

    def act(self, x, action_mask: Optional[torch.Tensor] = None, storm_type_idx: Optional[int] = None):
        logits, value = self.forward(x, action_mask, storm_type_idx)
        probs = torch.softmax(logits, dim=-1)
        dist = torch.distributions.Categorical(probs)
        action = dist.sample()
        return action, dist.log_prob(action), dist.entropy(), value.squeeze(-1)


