def calculate_storm_differentiation(action_distributions: Dict[str, Dict[int, float]]) -> float:
    """Calculate JSD between storm-type action distributions"""
    storm_types = list(action_distributions.keys())
    if len(storm_types) < 2:
        return 0.0

    def to_prob_array(dist: Dict[int, float]) -> np.ndarray:
        arr = np.zeros(10)
        total = sum(dist.values())
        if total > 0:
            for action, count in dist.items():
                arr[action] = count / total
        else:
            arr = np.ones(10) / 10
        arr = arr + 1e-10
        arr = arr / arr.sum()
        return arr

    jsd_values = []
    for i, st1 in enumerate(storm_types):
        for st2 in storm_types[i+1:]:
            p = to_prob_array(action_distributions[st1])
            q = to_prob_array(action_distributions[st2])
            jsd = jensenshannon(p, q)
            jsd_values.append(jsd)

    return np.mean(jsd_values) if jsd_values else 0.0

