class PerStormLogger:
    """
    Tracks and logs action distributions for each storm type separately.
    Enables easy visualization of differentiation (or lack thereof).
    """

    def __init__(self, config: Config):
        self.config = config
        self.storm_types = [StormType.SURGE, StormType.RAIN, StormType.WIND, StormType.MIXED]
        self.action_names = ['Monitor', 'Advisory', 'CloseBeach', 'SandbagC',
                            'SandbagR', 'EvacHigh', 'EvacCoast', 'EvacRiver',
                            'ActProt', 'FullEvac']

        # Running totals (reset each checkpoint)
        self.reset_window()

        # History for plotting
        self.history = {
            'episodes': [],
            'jsd': [],
            'per_storm_distributions': [],
            'per_storm_monitor_rates': [],
        }

    def reset_window(self):
        """Reset counters for new evaluation window"""
        self.window_actions = {st: {i: 0 for i in range(10)} for st in self.storm_types}
        self.window_count = {st: 0 for st in self.storm_types}

    def record_action(self, storm_type: str, action: int):
        """Record an action for a storm type"""
        self.window_actions[storm_type][action] += 1
        self.window_count[storm_type] += 1

    def get_distributions(self) -> Dict[str, Dict[int, float]]:
        """Get normalized action distributions for each storm type"""
        distributions = {}
        for st in self.storm_types:
            total = sum(self.window_actions[st].values())
            if total > 0:
                distributions[st] = {a: c / total for a, c in self.window_actions[st].items()}
            else:
                distributions[st] = {a: 0.0 for a in range(10)}
        return distributions

    def calculate_jsd(self) -> float:
        """Calculate Jensen-Shannon divergence between storm type distributions"""
        distributions = self.get_distributions()

        def to_prob_array(dist):
            arr = np.array([dist.get(i, 0) for i in range(10)])
            arr = arr + 1e-10  # Avoid division by zero
            return arr / arr.sum()

        jsd_values = []
        for i, st1 in enumerate(self.storm_types):
            for st2 in self.storm_types[i+1:]:
                p = to_prob_array(distributions[st1])
                q = to_prob_array(distributions[st2])
                jsd_values.append(jensenshannon(p, q))

        return np.mean(jsd_values) if jsd_values else 0.0

    def log_checkpoint(self, episode: int, writer: SummaryWriter = None):
        """Log current distributions at checkpoint"""
        distributions = self.get_distributions()
        jsd = self.calculate_jsd()

        # Store in history
        self.history['episodes'].append(episode)
        self.history['jsd'].append(jsd)
        self.history['per_storm_distributions'].append(deepcopy(distributions))

        # Calculate Monitor rates per storm
        monitor_rates = {}
        for st in self.storm_types:
            total = sum(self.window_actions[st].values())
            monitor_rates[st] = self.window_actions[st][0] / total * 100 if total > 0 else 0
        self.history['per_storm_monitor_rates'].append(monitor_rates)

        # Print detailed breakdown
        print(f"\n📊 Per-Storm Action Distributions (Episode {episode}):")
        print("-" * 70)
        for st in self.storm_types:
            total = sum(self.window_actions[st].values())
            if total > 0:
                dist_str = f"  {st:6s}: "
                sorted_actions = sorted(self.window_actions[st].items(), key=lambda x: x[1], reverse=True)
                for a, c in sorted_actions[:5]:  # Top 5 actions
                    pct = c / total * 100
                    if pct >= 1:
                        dist_str += f"{self.action_names[a]}:{pct:.0f}% "
                print(dist_str)

        print(f"\n  Storm Differentiation (JSD): {jsd:.4f}")
        if jsd < 0.03:
            print(f"  ⚠️ CRITICAL: Near-zero differentiation - generic policy!")
        elif jsd < 0.05:
            print(f"  ⚠️ LOW: Minimal storm-type adaptation")
        elif jsd < 0.10:
            print(f"  ⚡ MODERATE: Some storm-type adaptation")
        else:
            print(f"  ✅ GOOD: Agent differentiates by storm type!")

        # Log to tensorboard
        if writer:
            writer.add_scalar('Differentiation/JSD', jsd, episode)
            for st in self.storm_types:
                writer.add_scalar(f'MonitorRate/{st}', monitor_rates[st], episode)

        # Reset for next window
        self.reset_window()

        return jsd

