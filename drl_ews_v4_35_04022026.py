class PPOAgent:
    """PPO Agent with storm-conditioned policy heads support"""

    def __init__(self, config: Config, obs_dim: int, device: torch.device):
        self.config = config
        self.device = device
        self.policy = PPOActorCritic(config, obs_dim).to(device)
        self.optimizer = optim.Adam(self.policy.parameters(), lr=config.lr)
        self.memory = []
        self.entropy_coef = config.entropy_coef

        self.current_lr = config.lr
        self.best_validation_score = 0.0
        self.episodes_since_improvement = 0

    def _get_storm_type_idx(self, obs: np.ndarray) -> int:
        """Extract storm type index from observation one-hot encoding.
        Storm type is at indices 10-13: [SURGE, RAIN, WIND, MIXED]
        """
        storm_one_hot = obs[10:14]
        return int(np.argmax(storm_one_hot))

    def select_action(self, obs: np.ndarray, action_mask: np.ndarray, training: bool = True, temperature: float = 1.0):
        obs_t = torch.FloatTensor(obs).unsqueeze(0).to(self.device)
        mask_t = torch.FloatTensor(action_mask).unsqueeze(0).to(self.device)


        storm_type_idx = self._get_storm_type_idx(obs)

        with torch.no_grad():
            logits, value = self.policy(obs_t, mask_t, storm_type_idx)

            if temperature == 0.0:
                action = logits.argmax(dim=-1)
                if training:
                    probs = torch.softmax(logits, dim=-1)
                    dist = torch.distributions.Categorical(probs)
                    return action.item(), dist.log_prob(action).item(), dist.entropy().item(), value.squeeze(-1).item()
                return action.item()

            logits = logits / temperature
            probs = torch.softmax(logits, dim=-1)
            dist = torch.distributions.Categorical(probs)

            if training:
                action = dist.sample()
                return action.item(), dist.log_prob(action).item(), dist.entropy().item(), value.squeeze(-1).item()
            else:
                action = dist.sample()
                return action.item()

    def store_transition(self, obs, action, reward, next_obs, done, log_prob, value, entropy, action_mask=None):

        storm_type_idx = self._get_storm_type_idx(obs)
        self.memory.append({
            'obs': obs, 'action': action, 'reward': reward, 'next_obs': next_obs,
            'done': done, 'log_prob': log_prob, 'value': value, 'entropy': entropy,
            'action_mask': action_mask, 'storm_type_idx': storm_type_idx
        })

    def compute_gae(self, rewards, values, dones, next_value):
        advantages, gae = [], 0
        values = values + [next_value]
        for t in reversed(range(len(rewards))):
            delta = rewards[t] + self.config.gamma * values[t + 1] * (1 - dones[t]) - values[t]
            gae = delta + self.config.gamma * self.config.gae_lambda * (1 - dones[t]) * gae
            advantages.insert(0, gae)
        return advantages, [adv + val for adv, val in zip(advantages, values[:-1])]

    def update(self):
        if not self.memory:
            return {}

        obs = torch.FloatTensor(np.array([t['obs'] for t in self.memory])).to(self.device)
        actions = torch.LongTensor([t['action'] for t in self.memory]).to(self.device)
        old_log_probs = torch.FloatTensor([t['log_prob'] for t in self.memory]).to(self.device)


        storm_type_indices = torch.LongTensor([t['storm_type_idx'] for t in self.memory]).to(self.device)

        masks = [t.get('action_mask') for t in self.memory]
        if masks[0] is not None:
            action_masks = torch.FloatTensor(np.array(masks)).to(self.device)
        else:
            action_masks = None

        # Get next value using the appropriate storm head
        with torch.no_grad():
            next_obs = torch.FloatTensor(self.memory[-1]['next_obs']).unsqueeze(0).to(self.device)
            next_storm_idx = self.memory[-1]['storm_type_idx']
            _, next_value = self.policy(next_obs, storm_type_idx=next_storm_idx)

        advantages, returns = self.compute_gae([t['reward'] for t in self.memory], [t['value'] for t in self.memory],
                                               [t['done'] for t in self.memory], next_value.item())
        advantages = torch.FloatTensor(advantages).to(self.device)
        returns = torch.FloatTensor(returns).to(self.device)
        advantages = (advantages - advantages.mean()) / (advantages.std() + 1e-8)

        total_loss, total_policy_loss, total_value_loss, total_entropy = 0, 0, 0, 0
        for _ in range(self.config.k_epochs):

            logits, values_pred = self.policy(obs, action_masks, storm_type_indices)
            probs = torch.softmax(logits, dim=-1)
            dist = torch.distributions.Categorical(probs)
            new_log_probs, entropy = dist.log_prob(actions), dist.entropy().mean()

            ratio = torch.exp(new_log_probs - old_log_probs)
            policy_loss = -torch.min(ratio * advantages, torch.clamp(ratio, 1 - self.config.eps_clip, 1 + self.config.eps_clip) * advantages).mean()
            value_loss = nn.MSELoss()(values_pred.squeeze(), returns)
            loss = policy_loss + self.config.value_loss_coef * value_loss - self.entropy_coef * entropy

            self.optimizer.zero_grad()
            loss.backward()
            nn.utils.clip_grad_norm_(self.policy.parameters(), self.config.max_grad_norm)
            self.optimizer.step()

            total_loss += loss.item()
            total_policy_loss += policy_loss.item()
            total_value_loss += value_loss.item()
            total_entropy += entropy.item()

        self.entropy_coef = max(self.config.min_entropy_coef, self.entropy_coef * self.config.entropy_decay)
        self.memory = []
        return {'loss': total_loss / self.config.k_epochs, 'entropy': total_entropy / self.config.k_epochs}

    def maybe_decay_lr(self, validation_score: float, episode: int) -> bool:
        if validation_score > self.best_validation_score:
            self.best_validation_score = validation_score
            self.episodes_since_improvement = 0
            return False

        self.episodes_since_improvement += self.config.checkpoint_interval

        if self.episodes_since_improvement >= self.config.lr_decay_patience:
            new_lr = max(self.current_lr * self.config.lr_decay_factor, self.config.lr_min)
            if new_lr < self.current_lr:
                self.current_lr = new_lr
                for param_group in self.optimizer.param_groups:
                    param_group['lr'] = new_lr
                self.episodes_since_improvement = 0
                print(f"  📉 LR decayed to {new_lr:.2e}")
                return True
        return False

    def save(self, path):
        torch.save({
            'policy_state_dict': self.policy.state_dict(),
            'optimizer_state_dict': self.optimizer.state_dict(),
            'entropy_coef': self.entropy_coef,
            'current_lr': self.current_lr,
            'best_validation_score': self.best_validation_score
        }, path)

    def load(self, path):
        checkpoint = torch.load(path, map_location=self.device, weights_only=False)
        self.policy.load_state_dict(checkpoint['policy_state_dict'])
        self.optimizer.load_state_dict(checkpoint['optimizer_state_dict'])
        self.entropy_coef = checkpoint.get('entropy_coef', self.config.entropy_coef)
        self.current_lr = checkpoint.get('current_lr', self.config.lr)
        self.best_validation_score = checkpoint.get('best_validation_score', 0.0)


