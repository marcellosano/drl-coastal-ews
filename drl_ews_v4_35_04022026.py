class TopKCheckpointManager:
    """Manages top-K checkpoints with composite scoring"""

    def __init__(self, config: Config, k: int = 5):
        self.config = config
        self.k = k
        self.checkpoints = []

    def calculate_composite_score(self, lives_saved: float, diversity: float,
                                   differentiation: float, max_action_rate: float) -> float:
        """Calculate composite score with stronger JSD weighting and low-JSD penalty"""
        # Base score components
        base_score = (
            self.config.checkpoint_lives_weight * lives_saved +
            self.config.checkpoint_diversity_weight * min(diversity / 7.0, 1.0) +
            self.config.checkpoint_differentiation_weight * min(differentiation / 0.1, 1.0)
        )


        # Bonus is proportional to differentiation, scaled by the bonus weight
        jsd_bonus = self.config.checkpoint_jsd_bonus_weight * min(differentiation / 0.15, 1.0) * 0.1
        base_score += jsd_bonus


        if differentiation < self.config.checkpoint_low_jsd_threshold:
            base_score -= self.config.checkpoint_low_jsd_penalty

        # Apply collapse penalty
        if max_action_rate > self.config.collapse_penalty_threshold:
            excess = max_action_rate - self.config.collapse_penalty_threshold
            penalty = min(excess * self.config.collapse_penalty_scale /
                         (1.0 - self.config.collapse_penalty_threshold),
                         self.config.collapse_penalty_scale)
            base_score *= (1.0 - penalty)

        return base_score

    def should_save(self, score: float) -> bool:
        if len(self.checkpoints) < self.k:
            return True
        return score > self.checkpoints[0][0]

    def save_checkpoint(self, agent, score: float, episode: int, metrics: Dict):
        path = os.path.join(self.config.checkpoint_dir, f'top_checkpoint_ep{episode}.pt')

        torch.save({
            'policy_state_dict': agent.policy.state_dict(),
            'optimizer_state_dict': agent.optimizer.state_dict(),
            'entropy_coef': agent.entropy_coef,
            'episode': episode,
            'score': score,
            'metrics': metrics
        }, path)

        if len(self.checkpoints) >= self.k:
            _, old_ep, old_path = heapq.heappop(self.checkpoints)
            if os.path.exists(old_path):
                os.remove(old_path)
                print(f"  🗑️ Removed old checkpoint: ep{old_ep}")

        heapq.heappush(self.checkpoints, (score, episode, path))
        print(f"  💾 Saved top checkpoint: ep{episode} (score={score:.4f})")

        return path

    def get_best_checkpoint(self) -> Optional[str]:
        if not self.checkpoints:
            return None
        best = max(self.checkpoints, key=lambda x: x[0])
        return best[2]

    def get_all_checkpoints(self) -> List[Tuple[float, int, str]]:
        return sorted(self.checkpoints, key=lambda x: x[0], reverse=True)


