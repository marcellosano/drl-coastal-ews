@dataclass
class RewardBreakdown:
    """Track individual reward components for debugging"""
    casualty_penalty: float = 0.0
    action_cost_penalty: float = 0.0
    blocked_route_penalty: float = 0.0
    zero_casualty_bonus: float = 0.0
    protection_bonus: float = 0.0
    early_action_bonus: float = 0.0
    strategic_bonus: float = 0.0
    total: float = 0.0

    def to_dict(self) -> Dict:
        return asdict(self)

    def __str__(self) -> str:
        return (f"Casualty: {-self.casualty_penalty:+.1f}, "
                f"ActionCost: {-self.action_cost_penalty:+.1f}, "
                f"ZeroBonus: {+self.zero_casualty_bonus:+.1f}, "
                f"Strategic: {+self.strategic_bonus:+.1f}, "
                f"Total: {self.total:+.1f}")

class RewardTracker:
    """Aggregate reward breakdowns across episodes"""

    def __init__(self):
        self.episode_breakdowns = []
        self.current_episode = RewardBreakdown()

    def add_step(self, breakdown: RewardBreakdown):
        self.current_episode.casualty_penalty += breakdown.casualty_penalty
        self.current_episode.action_cost_penalty += breakdown.action_cost_penalty
        self.current_episode.blocked_route_penalty += breakdown.blocked_route_penalty
        self.current_episode.zero_casualty_bonus += breakdown.zero_casualty_bonus
        self.current_episode.protection_bonus += breakdown.protection_bonus
        self.current_episode.early_action_bonus += breakdown.early_action_bonus
        self.current_episode.strategic_bonus += breakdown.strategic_bonus
        self.current_episode.total += breakdown.total

    def end_episode(self):
        self.episode_breakdowns.append(self.current_episode)
        self.current_episode = RewardBreakdown()

    def get_recent_average(self, n: int = 100) -> RewardBreakdown:
        if not self.episode_breakdowns:
            return RewardBreakdown()

        recent = self.episode_breakdowns[-n:]
        avg = RewardBreakdown()
        for bd in recent:
            avg.casualty_penalty += bd.casualty_penalty / len(recent)
            avg.action_cost_penalty += bd.action_cost_penalty / len(recent)
            avg.blocked_route_penalty += bd.blocked_route_penalty / len(recent)
            avg.zero_casualty_bonus += bd.zero_casualty_bonus / len(recent)
            avg.protection_bonus += bd.protection_bonus / len(recent)
            avg.early_action_bonus += bd.early_action_bonus / len(recent)
            avg.strategic_bonus += bd.strategic_bonus / len(recent)
            avg.total += bd.total / len(recent)
        return avg


