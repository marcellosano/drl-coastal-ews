def train(config: Config, device: torch.device, base_path: str):
    env = CoastalEnvironment(config, difficulty=0.5)
    obs_dim = env.observation_space.shape[0]

    print(f"\nObservation dimension: {obs_dim}")
    print(f"Action dimension: {config.num_actions}")

    agent = PPOAgent(config, obs_dim, device)
    curriculum = CurriculumManager(config)
    visualizer = TrainingVisualizer(config)
    checkpoint_manager = TopKCheckpointManager(config, k=config.num_top_checkpoints)
    reward_tracker = RewardTracker()
    writer = SummaryWriter(log_dir=config.log_dir)


    early_stopping = EarlyStoppingTracker(config)
    per_storm_logger = PerStormLogger(config)

    metrics = {
        'rewards': [], 'casualties': [], 'lives_saved': [],
        'curriculum_levels': [], 'action_diversity': [],
        'validation_episodes': [], 'validation_scores': [],
        'action_distribution_history': [],
        'storm_type_performance': {},
        'final_action_by_storm': {},
        'transit_casualties': [],
        'storm_differentiation': [],
        'differentiation_episodes': [],
        'learning_rates': [],
        'reward_breakdown_history': {},
    }

    storm_type_actions = {st: {i: 0 for i in range(10)} for st in [StormType.SURGE, StormType.RAIN, StormType.WIND, StormType.MIXED]}
    storm_type_results = {st: [] for st in [StormType.SURGE, StormType.RAIN, StormType.WIND, StormType.MIXED]}

    best_validation_score = 0.0

    print("\n" + "=" * 70)
    print("STARTING TRAINING")
    print("=" * 70 + "\n")


    print("📊 Generating sample storm trajectory visualizations...")
    storm_viz_dir = os.path.join(config.plot_dir, 'storm_samples')
    visualize_sample_storms(config, storm_viz_dir)

    for episode in range(1, config.total_episodes + 1):
        difficulty = curriculum.get_mixed_difficulty()
        env.difficulty = difficulty
        obs, info = env.reset()

        episode_reward = 0
        episode_actions = []
        storm_type = env.storm.storm_type

        for step in range(config.episode_steps):
            action_mask = env.get_action_mask()
            action, log_prob, entropy, value = agent.select_action(obs, action_mask, training=True)
            next_obs, reward, done, truncated, info = env.step(action)

            if 'reward_breakdown' in info:
                reward_tracker.add_step(info['reward_breakdown'])

            agent.store_transition(obs, action, reward, next_obs, done, log_prob, value, entropy, action_mask)

            episode_reward += reward
            episode_actions.append(action)
            storm_type_actions[storm_type][action] += 1


            per_storm_logger.record_action(storm_type, action)

            obs = next_obs
            if done:
                break

        reward_tracker.end_episode()

        total_pop = sum(h.population for h in env.houses)
        lives_saved = 1.0 - (env.total_casualties / total_pop)
        unique_actions = len(set(episode_actions))

        agent.update()
        advanced = curriculum.update(episode_reward, lives_saved)

        metrics['rewards'].append(episode_reward)
