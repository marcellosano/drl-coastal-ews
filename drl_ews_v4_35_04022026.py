class HazardClusterer:
    """
    Multi-hazard clustering on grid-based hazard intensities.

    Process:
    1. Sample grid points every N cells
    2. Calculate normalized surge, rain, wind hazard at each point
    3. Include node if ANY hazard exceeds threshold (catches single & multi-hazard)
    4. DBSCAN clusters hazard hotspots spatially
    5. Extract 8 features per cluster

    Returns: 10 clusters × 8 features = 80 total features
    """

    def __init__(self, config: Config):
        self.config = config
        self.dbscan = DBSCAN(eps=config.dbscan_eps, min_samples=config.dbscan_min_samples)

        # Max hazard values for normalization (from storm characteristics)
        self.max_surge = 1.2
        self.max_rain = 1.2
        self.max_wind = 1.2

    def _calculate_hazard_at_point(self, x: int, y: int, storm) -> Tuple[float, float, float]:
        """Calculate normalized surge, rain, wind hazard intensities at a grid point."""

        # Distance from storm center
        dx, dy = x - storm.center_x, y - storm.center_y
        dist = np.sqrt(dx**2 + dy**2)

        # Wind exposure - based on distance from storm center (decays with radius)
        if dist <= storm.radius:
            wind_raw = storm.wind_speed * max(0, 1.0 - dist / storm.radius)
        else:
            wind_raw = 0.0

        # Surge exposure - based on distance from ocean (exponential decay inland)
        # Only applies if storm is approaching (center_x >= x means storm is at or past this point)
        ocean_dist = abs(x - self.config.ocean_x_start)
        if storm.center_x >= x - 5:  # Storm is approaching or arrived
            surge_raw = storm.surge_height * np.exp(-ocean_dist / 5.0)
        else:
            surge_raw = 0.0

        # Rain/flood exposure - based on distance from rivers (exponential decay)
        river_dist = min(
            abs(y - self.config.river_north_y),
            abs(y - self.config.river_south_y)
        )
        rain_raw = storm.rainfall_rate * np.exp(-river_dist / 4.0)

        # Normalize to 0-1 scale
        surge_norm = min(surge_raw / self.max_surge, 1.0)
        rain_norm = min(rain_raw / self.max_rain, 1.0)
        wind_norm = min(wind_raw / self.max_wind, 1.0)

        return surge_norm, rain_norm, wind_norm

    def get_cluster_features(self, houses, storm, current_step: int, max_steps: int) -> List[float]:
        """
        Multi-hazard clustering across grid space.
        Returns 8 features per cluster × max_clusters = 80 total features.
        """

        # Sample grid points
        points = []
        hazards = []  # List of (surge, rain, wind) tuples

        step = self.config.grid_sample_step
        for x in range(0, self.config.grid_size, step):
            for y in range(0, self.config.grid_size, step):
                surge, rain, wind = self._calculate_hazard_at_point(x, y, storm)
                max_hazard = max(surge, rain, wind)

                # Include if ANY single hazard exceeds threshold
                if max_hazard >= self.config.hazard_threshold:
                    points.append([x, y])
                    hazards.append((surge, rain, wind))

        # If no high-hazard points, return zeros
        if len(points) < self.config.dbscan_min_samples:
            return [0.0] * self.config.num_cluster_features

        points = np.array(points)
        hazards = np.array(hazards)  # Shape: (N, 3)

        # Run DBSCAN clustering on spatial positions
        labels = self.dbscan.fit_predict(points)

        # Extract cluster features
        clusters = []
        unique_labels = set(labels)
        unique_labels.discard(-1)  # Remove noise label

        for label in sorted(unique_labels):
            mask = labels == label
            cluster_points = points[mask]
            cluster_hazards = hazards[mask]  # Shape: (cluster_size, 3)

            # Centroid (normalized to 0-1)
            centroid_x = np.mean(cluster_points[:, 0]) / self.config.grid_size
            centroid_y = np.mean(cluster_points[:, 1]) / self.config.grid_size

            # Mean hazard intensities
            mean_surge = np.mean(cluster_hazards[:, 0])
            mean_rain = np.mean(cluster_hazards[:, 1])
            mean_wind = np.mean(cluster_hazards[:, 2])

            # Max intensity (across all hazard types)
            max_intensity = np.max(cluster_hazards)

            # Size (normalized)
            size = len(cluster_points) / 50.0  # Normalize by expected max

            # Multi-hazard ratio: fraction of nodes with 2+ hazards above threshold
            multi_hazard_count = 0
            for h in cluster_hazards:
                num_hazards = sum(1 for val in h if val >= self.config.hazard_threshold)
                if num_hazards >= 2:
                    multi_hazard_count += 1
            multi_hazard_ratio = multi_hazard_count / len(cluster_hazards)

            clusters.append({
                'centroid_x': centroid_x,
                'centroid_y': centroid_y,
                'mean_surge': mean_surge,
                'mean_rain': mean_rain,
                'mean_wind': mean_wind,
                'max_intensity': max_intensity,
                'size': size,
                'multi_hazard_ratio': multi_hazard_ratio
            })

        # Sort by max_intensity (most dangerous first)
        clusters.sort(key=lambda c: c['max_intensity'], reverse=True)

        # Build feature vector: 8 features per cluster
        features = []
        for i in range(self.config.max_clusters):
            if i < len(clusters):
                c = clusters[i]
                features.extend([
                    c['centroid_x'],
                    c['centroid_y'],
                    c['mean_surge'],
                    c['mean_rain'],
                    c['mean_wind'],
                    c['max_intensity'],
                    c['size'],
                    c['multi_hazard_ratio']
                ])
            else:
                # Padding for missing clusters
                features.extend([0.0] * self.config.cluster_feature_dim)

        return features


