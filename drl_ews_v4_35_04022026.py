class CurriculumManager:
    def __init__(self, config: Config):
        self.config = config
        self.current_level = 0
        self.level_episodes = 0
        self.lives_saved_history = []

    def get_difficulty(self) -> float:
        return np.clip(self.current_level / self.config.num_curriculum_levels, 0.0, 1.0)

    def update(self, episode_reward: float, lives_saved_rate: float) -> bool:
        self.level_episodes += 1
        self.lives_saved_history.append(lives_saved_rate)

        if self.level_episodes < self.config.curriculum_min_episodes_per_level:
            return False

        window = min(self.config.curriculum_window_size, len(self.lives_saved_history))
        recent_lives = self.lives_saved_history[-window:]
        avg_lives = np.mean(recent_lives)

        if avg_lives >= self.config.curriculum_lives_threshold:
            if self.current_level < self.config.num_curriculum_levels - 1:
                self.current_level += 1
                self.level_episodes = 0
                self.lives_saved_history = []
                return True
        return False

    def get_mixed_difficulty(self) -> float:
        """Add high-intensity bias for late curriculum levels.
        V4.34: Also injects false-alarm (very low difficulty) episodes at all
        curriculum levels so the agent learns restraint."""
        if not self.config.mixed_difficulty_enabled:
            return self.get_difficulty()

        # V4.34: False alarm injection — applies at ALL curriculum levels
        # Ensures agent regularly sees storms where restraint is correct
        if np.random.random() < self.config.false_alarm_rate:
            return np.random.uniform(0.0, self.config.false_alarm_max_difficulty)

        # Uses Beta(2.0, 1.0) which has mean ~0.67 instead of uniform 0.5
        if self.current_level >= self.config.curriculum_high_intensity_bias_level:
            # Apply intensity bias to difficulty sampling
            base_difficulty = self.get_difficulty()
            # Use beta distribution to bias upward
            intensity_modifier = np.random.beta(2.0, 1.0)  # Mean ~0.67
            # Scale the modifier to add to base difficulty
            return min(1.0, base_difficulty * 0.3 + intensity_modifier * 0.7)

        # Standard mixed difficulty for early levels
        rand = np.random.random()
        if rand < self.config.current_level_ratio:
            return self.get_difficulty()
        elif rand < self.config.current_level_ratio + self.config.preview_level_ratio:
            return min(self.current_level + 1, self.config.num_curriculum_levels - 1) / self.config.num_curriculum_levels
        return min(self.current_level + 2, self.config.num_curriculum_levels - 1) / self.config.num_curriculum_levels


