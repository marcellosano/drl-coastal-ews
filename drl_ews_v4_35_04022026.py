class EarlyStoppingTracker:
    """
    Tracks JSD and survival to trigger early stopping when:
    1. Curriculum level >= threshold
    2. Survival has plateaued (high and stable)
    3. JSD has collapsed for N consecutive windows
    """

    def __init__(self, config: Config):
        self.config = config
        self.jsd_history = []
        self.survival_history = []
        self.curriculum_history = []
        self.jsd_collapse_count = 0
        self.should_stop = False
        self.stop_reason = None
        self.peak_jsd = 0.0
        self.peak_jsd_episode = 0

    def update(self, episode: int, jsd: float, survival: float, curriculum_level: int) -> bool:
        """
        Update tracker with new evaluation results.
        Returns True if training should stop.
        """
        self.jsd_history.append(jsd)
        self.survival_history.append(survival)
        self.curriculum_history.append(curriculum_level)

        # Track peak JSD
        if jsd > self.peak_jsd:
            self.peak_jsd = jsd
            self.peak_jsd_episode = episode

        if not self.config.use_early_stopping:
            return False

        # Check if conditions for early stopping are met
        if curriculum_level < self.config.early_stop_min_curriculum:
            self.jsd_collapse_count = 0  # Reset collapse count if curriculum not reached
            return False

        # Check survival plateau
        if len(self.survival_history) >= 3:
            recent_survival = self.survival_history[-3:]
            avg_survival = np.mean(recent_survival)
            survival_var = np.var(recent_survival)

            survival_plateaued = (
                avg_survival >= self.config.early_stop_min_survival and
                survival_var < self.config.early_stop_survival_variance_threshold
            )
        else:
            survival_plateaued = False

        # Check JSD collapse
        jsd_collapsed = jsd < self.config.early_stop_jsd_threshold

        if jsd_collapsed:
            self.jsd_collapse_count += 1
        else:
            self.jsd_collapse_count = 0

        # Trigger early stopping
        if (survival_plateaued and
            self.jsd_collapse_count >= self.config.early_stop_jsd_collapse_windows):
            self.should_stop = True
            self.stop_reason = (
                f"Early stopping triggered at episode {episode}:\n"
                f"  - Curriculum level: {curriculum_level} (>= {self.config.early_stop_min_curriculum})\n"
                f"  - Survival plateaued: {avg_survival*100:.1f}% (>= {self.config.early_stop_min_survival*100:.0f}%)\n"
                f"  - JSD collapsed: {jsd:.4f} for {self.jsd_collapse_count} windows (< {self.config.early_stop_jsd_threshold})\n"
                f"  - Peak JSD was {self.peak_jsd:.4f} at episode {self.peak_jsd_episode}"
            )
            return True

        return False

    def get_status(self) -> str:
        """Get current status string for logging"""
        if len(self.jsd_history) == 0:
            return "No data yet"

        return (
            f"JSD: {self.jsd_history[-1]:.4f} (peak: {self.peak_jsd:.4f}@ep{self.peak_jsd_episode}), "
            f"Collapse count: {self.jsd_collapse_count}/{self.config.early_stop_jsd_collapse_windows}"
        )


