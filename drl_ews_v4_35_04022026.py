def validate(agent: PPOAgent, config: Config, device: torch.device, train_env: CoastalEnvironment, temperature: float = 0.0, num_episodes: int = 20) -> Dict:
    """Run validation episodes and return metrics"""
    env = CoastalEnvironment(config, difficulty=0.5)

    results = {'lives_saved': [], 'actions': [], 'transit_casualties': []}
    storm_type_actions = {st: {} for st in [StormType.SURGE, StormType.RAIN, StormType.WIND, StormType.MIXED]}

    for ep in range(num_episodes):
        obs, _ = env.reset()
        storm_type = env.storm.storm_type

        for step in range(config.episode_steps):
            action_mask = env.get_action_mask()
            action = agent.select_action(obs, action_mask, training=False, temperature=temperature)
            results['actions'].append(action)

            storm_type_actions[storm_type][action] = storm_type_actions[storm_type].get(action, 0) + 1

            obs, _, done, _, _ = env.step(action)
            if done:
                break

        total_pop = sum(h.population for h in env.houses)
        lives_saved = 1.0 - (env.total_casualties / total_pop)
        results['lives_saved'].append(lives_saved)
        results['transit_casualties'].append(env.transit_casualties)

    lives_saved = np.mean(results['lives_saved'])
    action_diversity = len(set(results['actions']))

    action_counts = {}
    for a in results['actions']:
        action_counts[a] = action_counts.get(a, 0) + 1
    total = len(results['actions'])

    max_action_rate = max(action_counts.values()) / total if total > 0 else 0

    storm_differentiation = calculate_storm_differentiation(storm_type_actions)

    return {
        'lives_saved': lives_saved,
        'action_diversity': action_diversity,
        'max_action_rate': max_action_rate,
        'storm_differentiation': storm_differentiation,
    }

def evaluate_with_analysis(agent: PPOAgent, config: Config, device: torch.device, num_episodes: int = 40):
    """Enhanced evaluation with per-storm-type action distributions"""
    env = CoastalEnvironment(config, difficulty=0.5)
    visualizer = TrainingVisualizer(config)


    correct_actions_map = {
        StormType.SURGE: [3, 6, 5, 9],   # Sandbag Coast, Evac Coastal, Evac High, Full Evac
        StormType.RAIN: [4, 7, 8, 9],     # Sandbag River, Evac River, Activate Prot (drainage), Full Evac
        StormType.WIND: [1, 8, 5, 9],     # Advisory (shelter-in-place!), Activate Prot, Evac High, Full Evac
        StormType.MIXED: [9, 6, 7, 5, 3, 4, 8]
    }

    for eval_mode, temperature in [("GREEDY (T=0)", 0.0), (f"SAMPLING (T={config.eval_temperature})", config.eval_temperature)]:
        results_by_type = {st: {'lives_saved': [], 'actions': [], 'transit_casualties': [], 'correct_actions': 0, 'total_actions': 0} for st in [StormType.SURGE, StormType.RAIN, StormType.WIND, StormType.MIXED]}

        print("\n" + "=" * 70)
        print(f"EVALUATION: {eval_mode}")
        print("=" * 70 + "\n")

        for ep in range(num_episodes):
            obs, _ = env.reset()
            storm_type = env.storm.storm_type
            episode_actions = []

            for step in range(config.episode_steps):
                action_mask = env.get_action_mask()
                action = agent.select_action(obs, action_mask, training=False, temperature=temperature)
                episode_actions.append(action)

                if action in correct_actions_map.get(storm_type, []):
                    results_by_type[storm_type]['correct_actions'] += 1
                results_by_type[storm_type]['total_actions'] += 1

                obs, _, done, _, _ = env.step(action)
                if done:
                    break

            total_pop = sum(h.population for h in env.houses)
            lives_saved = 1.0 - (env.total_casualties / total_pop)
            results_by_type[storm_type]['lives_saved'].append(lives_saved)
            results_by_type[storm_type]['actions'].extend(episode_actions)
            results_by_type[storm_type]['transit_casualties'].append(env.transit_casualties)

            print(f"Episode {ep+1}: Storm={storm_type}, Lives={lives_saved*100:.1f}%, UniqueActions={len(set(episode_actions))}, TransitCasualties={env.transit_casualties:.2f}")

        print("\n" + "=" * 70)
        print(f"RESULTS BY STORM TYPE ({eval_mode})")
        print("=" * 70)

        action_distributions = {}

        for st in [StormType.SURGE, StormType.RAIN, StormType.WIND, StormType.MIXED]:
            data = results_by_type[st]
            if data['lives_saved']:
                correct_rate = data['correct_actions'] / data['total_actions'] * 100 if data['total_actions'] > 0 else 0
                print(f"\n{st}: Avg Lives Saved = {np.mean(data['lives_saved'])*100:.1f}%, Transit = {np.mean(data['transit_casualties']):.2f}, CorrectActions = {correct_rate:.0f}%")
                action_counts = {}
                for a in data['actions']:
                    action_counts[a] = action_counts.get(a, 0) + 1
                total = len(data['actions'])

                action_distributions[st] = action_counts

                max_action_pct = max(action_counts.values()) / total * 100 if total > 0 else 0
                max_action_id = max(action_counts, key=action_counts.get) if action_counts else 0
                if max_action_pct > 70:
                    print(f"  ⚠️ COLLAPSE WARNING: {visualizer.action_names[max_action_id]} = {max_action_pct:.0f}%")

                print(f"  Action Distribution: ", end="")
                for a, c in sorted(action_counts.items(), key=lambda x: x[1], reverse=True):
                    pct = c/total*100
                    if pct >= 1:
                        print(f"{visualizer.action_names[a]}: {pct:.1f}%  ", end="")
                print()

        diff = calculate_storm_differentiation(action_distributions)
        print(f"\n📊 Storm-Type Differentiation (JSD): {diff:.3f}")
        if diff < 0.05:
            print("  ⚠️ LOW DIFFERENTIATION: Agent not adapting to storm type!")
        elif diff >= 0.1:
            print("  ✅ GOOD DIFFERENTIATION: Agent adapts behavior to storm type")
        else:
            print("  ⚡ MODERATE DIFFERENTIATION: Some storm-type adaptation")

    return results_by_type


