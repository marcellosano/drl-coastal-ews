class Storm:
    """
    Realistic Storm Lifecycle Simulator

    Storms follow a realistic approach → peak → retreat pattern:
    - Start at low intensity (~0.1)
    - Build to peak around step 12-18 (configurable)
    - Decay as storm passes

    Storm-type specific patterns:
    - SURGE: Tidal modulation (high/low tide effects)
    - RAIN: Dual-peak (outer bands + main rainfall)
    - WIND: Gusty profile with high-frequency noise
    - MIXED: Combination of all patterns
    """

    def __init__(self, config: Config, difficulty: float = 0.5, storm_type: str = None):
        self.config = config
        self.difficulty = difficulty
        self.storm_type = storm_type or random.choice([StormType.SURGE, StormType.RAIN, StormType.WIND, StormType.MIXED])

        # Storm position and movement
        self.center_x = config.grid_size + 5
        self.center_y = np.random.randint(5, config.grid_size - 5)
        self.direction = np.pi + np.random.uniform(-0.3, 0.3)
        self.movement_speed = 0.3 + 0.4 * difficulty


        self.current_step = 0
        self._precompute_trajectory()

        # Set initial values from trajectory
        self._apply_trajectory_step(0)

    def _precompute_trajectory(self):
        """
        Pre-compute realistic storm intensity curves for entire episode.
        Creates separate curves for surge, rain, wind based on storm type.
        """
        config = self.config
        num_steps = config.storm_steps

        # Determine peak step (with randomization)
        self.peak_step = np.clip(
            int(np.random.normal(config.storm_peak_step_mean, config.storm_peak_step_std)),
            config.storm_peak_step_min,
            config.storm_peak_step_max
        )

        # Peak intensity based on difficulty
        # V4.34: Lowered floor so difficulty 0.0-0.20 produces storms BELOW
        # the severity gating threshold (0.25), forcing the agent to learn restraint
        # V4.33: 0.4 + 0.5 * difficulty → range [0.35, 1.0] (every storm exceeded gate)
        # V4.34: 0.1 + 0.8 * difficulty → range [0.05, 1.0]
        self.peak_intensity = 0.10 + 0.80 * self.difficulty + np.random.uniform(-0.08, 0.08)
        self.peak_intensity = np.clip(self.peak_intensity, 0.05, 1.0)

        # Generate base intensity envelope (asymmetric bell curve)
        self.intensity_curve = self._generate_base_envelope(num_steps)

        # Generate storm-type-specific hazard curves
        self.surge_curve = np.zeros(num_steps)
        self.rain_curve = np.zeros(num_steps)
        self.wind_curve = np.zeros(num_steps)

        if self.storm_type == StormType.SURGE:
            self.surge_curve = self._generate_surge_curve(num_steps)
            self.rain_curve = self.intensity_curve * 0.2 + np.random.uniform(-0.02, 0.02, num_steps)
            self.wind_curve = self.intensity_curve * 0.4 + np.random.uniform(-0.03, 0.03, num_steps)

        elif self.storm_type == StormType.RAIN:
            self.rain_curve = self._generate_rain_curve(num_steps)
            self.surge_curve = self.intensity_curve * 0.2 + np.random.uniform(-0.02, 0.02, num_steps)
            self.wind_curve = self.intensity_curve * 0.3 + np.random.uniform(-0.03, 0.03, num_steps)

        elif self.storm_type == StormType.WIND:
            self.wind_curve = self._generate_wind_curve(num_steps)
            self.surge_curve = self.intensity_curve * 0.3 + np.random.uniform(-0.02, 0.02, num_steps)
            self.rain_curve = self.intensity_curve * 0.3 + np.random.uniform(-0.03, 0.03, num_steps)

        else:  # MIXED - combination of all
            self.surge_curve = self._generate_surge_curve(num_steps) * 0.85
            self.rain_curve = self._generate_rain_curve(num_steps) * 0.85
            self.wind_curve = self._generate_wind_curve(num_steps) * 0.85

        # Ensure non-negative values
        self.surge_curve = np.clip(self.surge_curve, 0.02, 1.2)
        self.rain_curve = np.clip(self.rain_curve, 0.02, 1.2)
        self.wind_curve = np.clip(self.wind_curve, 0.02, 1.2)
        self.intensity_curve = np.clip(self.intensity_curve, 0.05, 1.0)

        # Pre-compute positions
        self.position_trajectory = []
        cx, cy = self.center_x, self.center_y
        for step in range(num_steps):
            self.position_trajectory.append((cx, cy))
            cx += self.movement_speed * np.cos(self.direction)
            cy += self.movement_speed * np.sin(self.direction)
            cy = np.clip(cy, 0, self.config.grid_size - 1)

    def _generate_base_envelope(self, num_steps: int) -> np.ndarray:
        """Generate asymmetric bell curve for base intensity envelope."""
        config = self.config
        envelope = np.zeros(num_steps)

        for step in range(num_steps):
            # Distance from peak (in steps)
            dist_from_peak = step - self.peak_step

            if dist_from_peak <= 0:
                # Approach phase - building intensity
                steepness = config.storm_approach_steepness
            else:
                # Retreat phase - fading intensity
                steepness = config.storm_retreat_steepness

            # Gaussian-like envelope
            decay = np.exp(-steepness * dist_from_peak**2)

            # Scale between base and peak intensity
            envelope[step] = config.storm_base_intensity + (self.peak_intensity - config.storm_base_intensity) * decay

            # Add small noise
            envelope[step] += np.random.uniform(-config.storm_intensity_noise, config.storm_intensity_noise)

        return envelope

    def _generate_surge_curve(self, num_steps: int) -> np.ndarray:
        """
        Generate surge intensity with tidal modulation.
        Tide raises/lowers surge by ±amplitude around base envelope.
        """
        config = self.config

        # Start with base envelope scaled for surge dominance
        surge = self.intensity_curve * 1.2

        # Add tidal modulation
        tidal_phase = np.random.uniform(0, 2 * np.pi) if config.storm_tidal_phase_random else 0
        hours_per_step = 3.0  # Each step is 3 hours

        for step in range(num_steps):
            hour = step * hours_per_step
            # Tidal cycle with slight irregularity
            tidal_cycle = 2 * np.pi * hour / config.storm_tidal_period
            tidal_factor = 1.0 + config.storm_tidal_amplitude * np.sin(tidal_cycle + tidal_phase)

            # Modulate surge (more effect near peak)
            intensity_factor = self.intensity_curve[step] / max(self.intensity_curve)
            surge[step] *= (1.0 + (tidal_factor - 1.0) * intensity_factor)

            # Add small noise
            surge[step] += np.random.uniform(-0.02, 0.02)

        return surge

    def _generate_rain_curve(self, num_steps: int) -> np.ndarray:
        """
        Generate rainfall with dual-peak pattern.
        First peak: Outer rain bands (before main storm)
        Second peak: Main rainfall (at storm center)
        """
        config = self.config

        if not config.storm_rain_dual_peak:
            # Simple single peak
            return self.intensity_curve * 1.2

        rain = np.zeros(num_steps)

        # First peak (outer bands) - earlier and weaker
        first_peak_step = max(0, self.peak_step + int(config.storm_rain_first_peak_offset))
        first_peak_intensity = self.peak_intensity * config.storm_rain_first_peak_strength

        for step in range(num_steps):
            # Main rainfall peak
            main_dist = step - self.peak_step
            main_decay = np.exp(-0.08 * main_dist**2)
            main_contribution = self.peak_intensity * 1.2 * main_decay

            # First peak (outer bands)
            first_dist = step - first_peak_step
            first_decay = np.exp(-0.15 * first_dist**2)  # Sharper peak
            first_contribution = first_peak_intensity * 1.2 * first_decay

            # Combine with slight overlap
            rain[step] = max(main_contribution, first_contribution * 0.7) + first_contribution * 0.3

            # Add rain band noise (irregular intensity)
            band_noise = config.storm_rain_band_noise * np.sin(step * 1.5 + np.random.uniform(0, np.pi))
            rain[step] += band_noise

            # Base minimum
            rain[step] = max(rain[step], config.storm_base_intensity)

        return rain

    def _generate_wind_curve(self, num_steps: int) -> np.ndarray:
        """
        Generate wind speed with gusty profile.
        High-frequency variations simulate gusts.
        """
        config = self.config

        # Start with base envelope scaled for wind dominance
        wind = self.intensity_curve * 1.2

        # Add gust variations
        gust_frequency = config.storm_wind_gust_frequency
        gust_phase = np.random.uniform(0, 2 * np.pi)

        for step in range(num_steps):
            # Multiple frequency components for realistic gusts
            gust1 = np.sin(2 * np.pi * gust_frequency * step / num_steps + gust_phase)
            gust2 = 0.5 * np.sin(2 * np.pi * gust_frequency * 2.3 * step / num_steps + gust_phase * 1.7)
            gust3 = 0.3 * np.sin(2 * np.pi * gust_frequency * 4.1 * step / num_steps + gust_phase * 0.5)

            gust_factor = 1.0 + config.storm_wind_gust_factor * (gust1 + gust2 + gust3) / 1.8

            # More gusts near peak
            intensity_factor = self.intensity_curve[step] / max(self.intensity_curve)
            wind[step] *= (1.0 + (gust_factor - 1.0) * intensity_factor)

            # Random noise
            wind[step] += np.random.uniform(-0.03, 0.03)

        return wind

    def _apply_trajectory_step(self, step: int):
        """Apply pre-computed trajectory values to current storm state."""
        step = min(step, len(self.intensity_curve) - 1)

        self.intensity = self.intensity_curve[step]
        self.surge_height = self.surge_curve[step]
        self.rainfall_rate = self.rain_curve[step]
        self.wind_speed = self.wind_curve[step]

        # Wave height derived from surge and wind
        self.wave_height = 0.5 * self.surge_height + 0.3 * self.wind_speed

        # Radius scales with intensity
        self.radius = 5 + 5 * self.intensity

        # Position from trajectory
        if step < len(self.position_trajectory):
            self.center_x, self.center_y = self.position_trajectory[step]

    def step(self):
        """Advance storm by one time step."""
        self.current_step += 1
        self._apply_trajectory_step(self.current_step)

    def get_state(self) -> StormState:
        return StormState(
            center_x=self.center_x, center_y=self.center_y, radius=self.radius,
            intensity=self.intensity, wind_speed=self.wind_speed, surge_height=self.surge_height,
            rainfall_rate=self.rainfall_rate, wave_height=self.wave_height,
            movement_speed=self.movement_speed, direction=self.direction,
            storm_type=self.storm_type
        )

    def get_trajectory_at_step(self, step: int) -> StormState:
        """Get storm state at a specific future step (for forecasting)."""
        step = min(step, len(self.intensity_curve) - 1)
        cx, cy = self.position_trajectory[step] if step < len(self.position_trajectory) else self.position_trajectory[-1]

        return StormState(
            center_x=cx, center_y=cy,
            radius=5 + 5 * self.intensity_curve[step],
            intensity=self.intensity_curve[step],
            wind_speed=self.wind_curve[step],
            surge_height=self.surge_curve[step],
            rainfall_rate=self.rain_curve[step],
            wave_height=0.5 * self.surge_curve[step] + 0.3 * self.wind_curve[step],
            movement_speed=self.movement_speed,
            direction=self.direction,
            storm_type=self.storm_type
        )


